/**
 * Файл: database.h
 * 
 * Заголовочный файл класса для работы с SQLite базой данных
 * 
 * Что делает этот класс:
 * - Хранит всех пользователей мессенджера
 * - Сохраняет личные сообщения между пользователями
 * - Управляет групповыми чатами и сообщениями в них
 * - Поддерживает мягкое удаление (сообщения не пропадают навсегда)
 * - Работает с часовым поясом Новосибирска (UTC+7)
 * 
 * Все методы потокобезопасны (внутри используется мьютекс)
 */

#ifndef DATABASE_H
#define DATABASE_H

#include <sqlite3.h>   // Библиотека SQLite для работы с базой данных
#include <string>      // Для std::string
#include <vector>      // Для std::vector (возврат списков)

/**
 * Класс для работы с базой данных мессенджера
 * 
 * ============================================================================
 * ТАБЛИЦЫ В БАЗЕ ДАННЫХ
 * ============================================================================
 * 
 * 1. users - пользователи
 *    - id: уникальный номер
 *    - username: имя пользователя (уникальное)
 *    - created_at: дата регистрации
 * 
 * 2. messages - личные сообщения
 *    - id: уникальный номер сообщения
 *    - from_user: отправитель
 *    - to_user: получатель
 *    - message: текст сообщения
 *    - timestamp: время отправки
 *    - deleted_by_from: удалено отправителем (1 = да)
 *    - deleted_by_to: удалено получателем (1 = да)
 *    - deleted_for_all: удалено для всех (1 = да)
 *    - reply_to_id: на какое сообщение ответ (NULL если не ответ)
 *    - forwarded_from: кто написал оригинал (NULL если не пересылка)
 * 
 * 3. groups - групповые чаты
 *    - id: уникальный номер группы
 *    - name: название группы (уникальное)
 *    - owner: кто создал группу (владелец)
 * 
 * 4. group_members - участники групп
 *    - group_name: название группы
 *    - username: имя участника
 *    (составной ключ: группа + пользователь)
 * 
 * 5. group_messages - сообщения в группах
 *    - id: уникальный номер сообщения
 *    - group_name: в какой группе
 *    - from_user: кто написал
 *    - message: текст
 *    - timestamp: время
 *    - reply_to_id: на какое сообщение ответ
 *    - forwarded_from: от кого переслано
 * 
 * 6. deleted_group_messages - скрытые (локально удалённые) сообщения в группах
 *    - username: кто скрыл
 *    - msg_id: ID скрытого сообщения
 *    (составной ключ: пользователь + сообщение)
 * 
 * ============================================================================
 * ОСОБЕННОСТИ РЕАЛИЗАЦИИ
 * ============================================================================
 * 
 * - Мягкое удаление: сообщения не удаляются физически из БД.
 *   Вместо этого устанавливаются флаги deleted_by_from/deleted_by_to.
 *   Это позволяет сохранить историю для аудита и даёт возможность
 *   одному участнику диалога удалить сообщение, а другому оставить.
 * 
 * - Постоянное хранение скрытых сообщений в группах:
 *   Раньше информация о том, какие сообщения пользователь скрыл,
 *   хранилась в оперативной памяти (map hidden_group_msg) и терялась
 *   при перезапуске клиента. Теперь эта информация сохраняется в
 *   таблице deleted_group_messages и не исчезает между сессиями.
 * 
 * - Часовой пояс: все даты и время хранятся в UTC, но при выборе
 *   прибавляется 7 часов для отображения по Новосибирскому времени.
 *   Это позволяет серверу работать в любом часовом поясе, а клиентам
 *   видеть единое время (Новосибирск).
 * 
 * - Потокобезопасность: все операции с БД защищены мьютексом db_mutex,
 *   что критически важно, так как сервер многопоточный и все потоки
 *   используют один экземпляр класса Database.
 */
class Database {
private:
    /**
     * Указатель на открытое соединение с SQLite базой данных
     * Инициализируется в init(), закрывается в деструкторе
     */
    sqlite3* db;
    
public:
    // ========================================================================
    // КОНСТРУКТОР И ДЕСТРУКТОР
    // ========================================================================
    
    /**
     * Конструктор
     * Просто обнуляет указатель на базу данных.
     * Реальное открытие БД происходит в методе init()
     */
    Database();
    
    /**
     * Деструктор
     * Закрывает соединение с базой данных, если оно было открыто
     */
    ~Database();
    
    // ========================================================================
    // ИНИЦИАЛИЗАЦИЯ
    // ========================================================================
    
    /**
     * Инициализация базы данных
     * 
     * Что делает:
     * 1. Открывает файл messenger.db (создаёт, если его нет)
     * 2. Создаёт все необходимые таблицы, если их нет
     * 
     * Нужно вызвать ОДИН РАЗ при запуске сервера
     * 
     * @return true - успех, false - ошибка (например, нет прав на запись)
     */
    bool init();
    
    // ========================================================================
    // РАБОТА С ПОЛЬЗОВАТЕЛЯМИ
    // ========================================================================
    
    /**
     * Создание нового пользователя
     * 
     * @param username - имя пользователя (не может быть пустым)
     * @return true - пользователь создан, false - ошибка
     * 
     * Пример: createUser("Alice")
     */
    bool createUser(const std::string& username);
    
    /**
     * Проверка существования пользователя
     * 
     * @param username - имя пользователя
     * @return true - пользователь есть в базе, false - нет
     * 
     * Пример: if (userExists("Bob")) { ... }
     */
    bool userExists(const std::string& username);
    
    // ========================================================================
    // ЛИЧНЫЕ СООБЩЕНИЯ
    // ========================================================================
    
    /**
     * Сохранение личного сообщения (упрощённая версия)
     * 
     * @param from_user - отправитель
     * @param to_user - получатель
     * @param message - текст сообщения
     * @return ID сохранённого сообщения, -1 при ошибке
     * 
     * Эту функцию удобно использовать для обычных сообщений
     * (не ответов, не пересылок). Внутри она просто вызывает
     * saveMessageAdvanced с reply_id=0 и пустым fwd_from
     * 
     * Пример: int msg_id = saveMessage("Alice", "Bob", "Привет!");
     */
    int saveMessage(const std::string& from_user, const std::string& to_user, 
                    const std::string& message);
    
    /**
     * Сохранение личного сообщения с расширенными возможностями
     * 
     * @param from_user - отправитель
     * @param to_user - получатель
     * @param message - текст сообщения
     * @param reply_id - ID сообщения, на которое отвечают (0 если не ответ)
     * @param fwd_from - имя автора оригинального сообщения (пусто если не пересылка)
     * @return ID сохранённого сообщения, -1 при ошибке
     * 
     * Примеры использования:
     * - Обычное сообщение: saveMessageAdvanced("Alice", "Bob", "Привет", 0, "")
     * - Ответ на сообщение №42: saveMessageAdvanced("Alice", "Bob", "Спасибо!", 42, "")
     * - Пересылка сообщения от Charlie: saveMessageAdvanced("Alice", "Bob", "Текст", 0, "Charlie")
     */
    int saveMessageAdvanced(const std::string& from_user, const std::string& to_user,
                           const std::string& message, int reply_id, 
                           const std::string& fwd_from);
    
    /**
     * Получение всей истории переписки между двумя пользователями
     * 
     * @param user1 - первый пользователь
     * @param user2 - второй пользователь
     * @return вектор строк, каждая строка в формате:
     *         "ID|ДАТА|ВРЕМЯ|ОТПРАВИТЕЛЬ|ТЕКСТ|REPLY_ID|FWD_FROM"
     * 
     * Формат даты: ДД.ММ.ГГГГ (например, "16.11.2023")
     * Формат времени: ЧЧ:ММ (например, "14:30")
     * 
     * Важно: сообщения, удалённые пользователем, не включаются в результат
     * Часовой пояс: Новосибирск (UTC+7)
     * 
     * Пример:
     *   auto msgs = getMessages("Alice", "Bob");
     *   for (auto& m : msgs) {
     *       // m = "42|16.11.2023|14:30|Alice|Привет|0|"
     *   }
     */
    std::vector<std::string> getMessages(const std::string& user1, 
                                         const std::string& user2);
    
    /**
     * Очистка истории переписки для конкретного пользователя
     * 
     * @param user - пользователь, который очищает историю
     * @param target - собеседник
     * @param for_all - true = удалить для всех (пока не поддерживается)
     * @return true при успехе
     * 
     * Как работает:
     * - Сообщения, отправленные user → target, помечаются как удалённые отправителем
     * - Сообщения, полученные user ← target, помечаются как удалённые получателем
     * - Сами сообщения остаются в БД, но user их больше не видит
     * - Другой участник диалога всё ещё видит эти сообщения
     * 
     * Пример: clearHistoryAdvanced("Alice", "Bob", false)
     */
    bool clearHistoryAdvanced(const std::string& user, const std::string& target, 
                              bool for_all);
    
    /**
     * Удаление одного сообщения для конкретного пользователя
     * 
     * @param msg_id - ID сообщения
     * @param user - пользователь, который удаляет
     * @param for_all - true = удалить для всех (пока не поддерживается)
     * @return true при успехе
     * 
     * Если user является отправителем, сообщение помечается deleted_by_from = 1
     * Если user является получателем, сообщение помечается deleted_by_to = 1
     * 
     * Пример: deleteMessageAdvanced(42, "Alice", false)
     */
    bool deleteMessageAdvanced(int msg_id, const std::string& user, bool for_all);
    
    /**
     * Проверка, были ли сегодня сообщения в диалоге
     * 
     * @param user1 - первый пользователь
     * @param user2 - второй пользователь
     * @return true - есть хотя бы одно сообщение от сегодняшнего дня
     * 
     * Используется для отрисовки разделителя "----- 16.11.2023 -----"
     * Разделитель показывается только при первом сообщении за день
     * 
     * Пример: if (!hasMessagesToday("Alice", "Bob")) { показать_разделитель(); }
     */
    bool hasMessagesToday(const std::string& user1, const std::string& user2);
    
    /**
     * Получение текста сообщения по его ID (для цитирования при ответах)
     * 
     * @param msg_id - ID сообщения
     * @param is_group - true = сообщение из группы, false = личное
     * @param reader_user - кто читает (проверка прав доступа)
     * @return текст в формате "автор: текст" или пустая строка
     * 
     * Для групповых сообщений проверка прав проще (нет мягкого удаления)
     * Для личных проверяет, не удалено ли сообщение для читающего
     * 
     * Пример: string quoted = getMessageText(42, false, "Alice");
     *          // quoted = "Bob: Привет, как дела?"
     */
    std::string getMessageText(int msg_id, bool is_group, 
                               const std::string& reader_user = "");
    
    // ========================================================================
    // ГРУППОВЫЕ ЧАТЫ
    // ========================================================================
    
    /**
     * Создание новой группы
     * 
     * @param group_name - название группы (уникальное, без пробелов)
     * @param owner - создатель группы (становится владельцем)
     * @return true при успехе
     * 
     * Создатель автоматически добавляется в участники группы
     * 
     * Пример: createGroup("C++ Developers", "Alice")
     */
    bool createGroup(const std::string& group_name, const std::string& owner);
    
    /**
     * Проверка, является ли пользователь владельцем группы
     * 
     * @param group_name - название группы
     * @param user - имя пользователя
     * @return true - пользователь - владелец
     * 
     * Владелец может:
     * - Добавлять участников (команда /gadd)
     * - Исключать участников (команда /gkick)
     * 
     * Пример: if (isGroupOwner("C++ Developers", "Alice")) { ... }
     */
    bool isGroupOwner(const std::string& group_name, const std::string& user);
    
    /**
     * Добавление пользователя в группу
     * 
     * @param group_name - название группы
     * @param user - имя пользователя
     * @return true при успехе
     * 
     * Если пользователь уже в группе, ничего не делает (не выдаёт ошибку)
     * 
     * Пример: addGroupMember("C++ Developers", "Bob")
     */
    bool addGroupMember(const std::string& group_name, const std::string& user);
    
    /**
     * Удаление пользователя из группы
     * 
     * @param group_name - название группы
     * @param user - имя пользователя
     * @return true при успехе
     * 
     * Пример: removeGroupMember("C++ Developers", "Bob")
     */
    bool removeGroupMember(const std::string& group_name, const std::string& user);
    
    /**
     * Проверка, состоит ли пользователь в группе
     * 
     * @param group_name - название группы
     * @param user - имя пользователя
     * @return true - пользователь в группе
     * 
     * Пример: if (isGroupMember("C++ Developers", "Alice")) { ... }
     */
    bool isGroupMember(const std::string& group_name, const std::string& user);
    
    /**
     * Получение списка всех участников группы
     * 
     * @param group_name - название группы
     * @return вектор имён пользователей
     * 
     * Пример: auto members = getGroupMembers("C++ Developers");
     *          // members = ["Alice", "Bob", "Charlie"]
     */
    std::vector<std::string> getGroupMembers(const std::string& group_name);
    
    /**
     * Проверка существования группы
     * 
     * @param group_name - название группы
     * @return true - группа существует
     * 
     * Пример: if (groupExists("C++ Developers")) { ... }
     */
    bool groupExists(const std::string& group_name);
    
    // ========================================================================
    // ГРУППОВЫЕ СООБЩЕНИЯ
    // ========================================================================
    
    /**
     * Сохранение сообщения в групповом чате (упрощённая версия)
     * 
     * @param group_name - название группы
     * @param from_user - отправитель
     * @param message - текст сообщения
     * @return ID сохранённого сообщения, -1 при ошибке
     * 
     * Пример: saveGroupMessage("C++ Developers", "Alice", "Всем привет!")
     */
    int saveGroupMessage(const std::string& group_name, const std::string& from_user, 
                         const std::string& message);
    
    /**
     * Сохранение сообщения в групповом чате с расширенными возможностями
     * 
     * @param group_name - название группы
     * @param from_user - отправитель
     * @param message - текст сообщения
     * @param reply_id - ID сообщения, на которое отвечают (0 если не ответ)
     * @param fwd_from - имя автора при пересылке (пусто если не пересылка)
     * @return ID сохранённого сообщения, -1 при ошибке
     * 
     * Примеры:
     * - Обычное: saveGroupMessageAdvanced("C++ Developers", "Alice", "Привет", 0, "")
     * - Ответ: saveGroupMessageAdvanced("C++ Developers", "Bob", "Согласен!", 42, "")
     * - Пересылка: saveGroupMessageAdvanced("C++ Developers", "Charlie", "Текст", 0, "David")
     */
    int saveGroupMessageAdvanced(const std::string& group_name, const std::string& from_user,
                                 const std::string& message, int reply_id, 
                                 const std::string& fwd_from);
    
    /**
     * Получение всех сообщений из группового чата
     * 
     * @param group_name - название группы
     * @return вектор строк в формате: "ID|ДАТА|ВРЕМЯ|ОТПРАВИТЕЛЬ|ТЕКСТ|REPLY_ID|FWD_FROM"
     * 
     * В групповых чатах нет мягкого удаления, поэтому показываются все сообщения
     * Локальное скрытие реализовано на уровне сервера (deleted_group_messages)
     * 
     * Пример: auto msgs = getGroupMessages("C++ Developers");
     */
    std::vector<std::string> getGroupMessages(const std::string& group_name);
    
    /**
     * Удаление сообщения из группы
     * 
     * @param msg_id - ID сообщения
     * @param user - пользователь, который удаляет
     * @param for_all - true = удалить для всех
     * @return true при успехе
     * 
     * ВНИМАНИЕ: в текущей версии не реализовано, всегда возвращает false
     * Пользователи могут скрывать сообщения локально через hideGroupMessageForUser
     * и getHiddenGroupMessages
     * 
     * TODO: Реализовать физическое удаление из БД для всех участников
     */
    bool deleteGroupMessage(int msg_id, const std::string& user, bool for_all);
    
    /**
     * Получение списка всех групп, в которых состоит пользователь
     * 
     * @param user - имя пользователя
     * @return вектор названий групп
     * 
     * Пример: auto myGroups = getUserGroups("Alice");
     *          // myGroups = ["C++ Developers", "Gamers"]
     */
    std::vector<std::string> getUserGroups(const std::string& user);
    
    // ========================================================================
    // НОВЫЕ МЕТОДЫ ДЛЯ ГРУППОВЫХ ЧАТОВ
    // (ПОСТОЯННОЕ ХРАНЕНИЕ СКРЫТЫХ СООБЩЕНИЙ В БД)
    // ========================================================================
    
    /**
     * Скрыть сообщение в группе для конкретного пользователя
     * 
     * @param username - имя пользователя
     * @param msg_id - ID сообщения
     * @return true при успехе
     * 
     * ПОЧЕМУ ЭТОТ МЕТОД НУЖЕН:
     * Раньше информация о скрытых сообщениях хранилась в оперативной памяти
     * (переменная hidden_group_msg в server.cpp) и терялась при перезапуске
     * клиента. Пользователь после перезапуска снова видел сообщения,
     * которые он удалил в прошлой сессии.
     * 
     * Теперь все скрытые сообщения сохраняются в таблице deleted_group_messages
     * и загружаются при каждом входе в групповой чат через getHiddenGroupMessages.
     * 
     * INSERT OR IGNORE - если пользователь уже скрыл это сообщение,
     * повторно не добавляем (избегаем дубликатов)
     */
    bool hideGroupMessageForUser(const std::string& username, int msg_id);
    
    /**
     * Получить список ID сообщений, скрытых пользователем в группах
     * 
     * @param username - имя пользователя
     * @return вектор ID скрытых сообщений
     * 
     * Вызывается при каждом входе пользователя в групповой чат.
     * Благодаря этому пользователь не видит сообщения, которые он скрыл
     * в прошлые сессии.
     * 
     * Пример использования в server.cpp:
     *   vector<int> hidden = db.getHiddenGroupMessages(username);
     *   if (find(hidden.begin(), hidden.end(), msg_id) != hidden.end()) {
     *       continue;  // Пропускаем скрытое сообщение
     *   }
     */
    std::vector<int> getHiddenGroupMessages(const std::string& username);
};

#endif
